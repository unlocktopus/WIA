short chooseKaprekar(short number) { //Elige aleatoriamente un n√∫mero de kaprekar de las lookup tables correspondiente al n√∫mero octal que se va a codificar
	switch (number) {
	case 0:
		return encoded0s[rand() % 10];
	case 1:
		return encoded1s[rand() % 383];
	case 2:
		return encoded2s[rand() % 576];
	case 3:
		return encoded3s[rand() % 2400];
	case 4:
		return encoded4s[rand() % 1272];
	case 5:
		return encoded5s[rand() % 1518];
	case 6:
		return encoded6s[rand() % 1656];
	case 7:
		return encoded7s[rand() % 2184];
	}
}

kaprekarUnit kaprekarEncode(unsigned char input) { //descompone un byte en tres componentes octales y asocia cada uno con un numero de Kaprekar
	kaprekarUnit unit; unit.top = 0; unit.middle = 0; unit.base = 0;

	unit.base = input & 7;
	unit.base = chooseKaprekar(unit.base);

	unit.middle = input & 56;
	unit.middle = unit.middle >> 3;
	unit.middle = chooseKaprekar(unit.middle);

	unit.top = input & 192;
	unit.top = unit.top >> 6;
	unit.top = chooseKaprekar(unit.top);

	return unit;
}

